public class BitwiseOperators {
    public static void main(String[] args) {
        
        // & (Binary AND)
        System.out.println("5&6="+(5&6));

        // | (Binary OR)
        System.out.println("5|6="+(5|6));

        // ^ (Binary OR)
        System.out.println("5^6="+(5^6));

        // ~ (Binary NOT)
        System.out.println("~6="+(~6));

        // >> (Binary Right Shift)
        System.out.println("5>>6="+(5>>6));

        // << (Binary Left Shift)
        System.out.println("5<<32="+(5<<32));
    }
}
